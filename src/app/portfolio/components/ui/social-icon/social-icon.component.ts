export class SocialIconComponent {
  @Input() link!: SocialLink;

  isGitHub(): boolean {
    return this.link.name.toLowerCase().includes('github') || 
           this.link.url.toLowerCase().includes('github');
  }

  isLinkedIn(): boolean {
    return this.link.name.toLowerCase().includes('linkedin') || 
           this.link.url.toLowerCase().includes('linkedin');
  }

  isInstagram(): boolean {
    return this.link.name.toLowerCase().includes('instagram') || 
           this.link.url.toLowerCase().includes('instagram');
  }

  isCodeChef(): boolean {
    return this.link.name.toLowerCase().includes('codechef') || 
           this.link.url.toLowerCase().includes('codechef');
  }

  getInitials(name: string): string {
    return name
      .split(' ')
      .map(word => word[0])
      .join('')
      .toUpperCase()
      .slice(0, 2);
  }
}





